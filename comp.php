<?php

$num1 = 5;
$num2 = 10;

// EQUAL TO 
if ($num1 == $num2) {
  echo $num1 . 'is equal to' . $num2;
} else {
  echo $num1 . 'is not equal to' . $num2;
}
echo "<br>";

// IsNOT EQUAL
if ($num1 != $num2) {
  echo $num1 . 'is  not equal to' . $num2;
} else {
  echo $num1 . 'is  equal to' . $num2;
}
echo "<br>";

// Less Than
if ($num1 < $num2) {
  echo $num1 . 'is  not equal to' . $num2;
} else {
  echo $num1 . 'is  equal to' . $num2;
}

echo "<br>";

// Greater Than
if ($num1 > $num2) {
  echo $num1 . 'is  not equal to' . $num2;
} else {
  echo $num1 . 'is  equal to' . $num2;
}

echo "<br>";

// Less Than OR Equal To

if ($num1 <= $num2) {
  echo $num1 . 'is  not equal to' . $num2;
} else {
  echo $num1 . 'is  equal to' . $num2;
}

echo "<br>";

// Greater Than OR Equal To

if ($num1 >= $num2) {
  echo $num1 . 'is  not equal to' . $num2;
} else {
  echo $num1 . 'is  equal to' . $num2;
}